% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


%------ACQUISIZIONE DATI DAL WORKSPACE E SISTEMAZIONE---------
ritardi_input = str2num(get(handles.ingressi,'String')) %acquisizione ritardi input
ritardi_output = str2num(get(handles.uscite,'String'))  %acquisizione ritardi output
if (ritardi_input>ritardi_output) && ritardi_output~=0  %controllo su fisica realizzabilità
    errordlg('Ritardi output < ritardi input','sistema non realizzabile');
else
datil= (evalin('base',get(handles.input,'String')))   %acquisizione dalla workspace del file di input
inputl= datil(:,str2num(get(handles.edit8,'String')))  %se colonna è 1 equivale a datil(:,1) tutte le righe ma solo la 1 colonna
assignin('base','inputl',inputl)
outputl= datil(:,str2num(get(handles.edit9,'String')))
assignin('base','outputl',outputl)
datit=(evalin('base',get(handles.INPUT_TEST,'String'))) %acquisizione dalla workspace del file di input test
inputt= datit(:,str2num(get(handles.edit10,'String')))
assignin('base','inputt',inputt)
outputt= datit(:,str2num(get(handles.edit11,'String')))
assignin('base','outputt',outputt)


%-----GENERAZIONE INPUT E OUTPUT AGLI ISTANTI PRECEDENTI
if ritardi_output ~=0
j=1+ (ritardi_output)- (ritardi_input) 
else
 j=1   
end

input_train=[] %inizializzo vettore di appoggio
input_test=[]
for i= ritardi_input:-1:0    %for(i=ritardi_input; i==0 ; i-- )
    input_train= [inputl(j:end-i,1) input_train];
    input_test= [inputt(j:end-i,1) input_test];
    j=j+1   
end
output_train=[]
output_test=[]
if ritardi_output ~=0 
j=1
else 
j= 1+ritardi_input;   
end

for i= ritardi_output:-1:0  %for(i=ritardi_output; i==0 ; i-- )
    output_train= [outputl(j:end-i,1) output_train];
    output_test= [outputt(j:end-i,1) output_test];
    j=j+1
end   
input_train = [input_train output_train(:,2:end)];
input_test = [input_test output_test(:,2:end)];

%generazione stringa da stampare
avviso = 'out(k) = F(in(k)'
for i= 0:1:ritardi_input-1
avviso = strcat(avviso , ', in(k-' , num2str(i+1) , ')' );
i=i+1
end
for i= 0:1:ritardi_output-1
avviso = strcat( avviso , ', out(k-' , num2str(i+1) ,')');
i=i+1
end
avviso = strcat(avviso , ')');

%--------esportazioni valori sulla workspace
assignin('base','input_train',input_train)
assignin('base','output_train',(output_train(:,:)))

%-------ACQUISIZIONE PARAMETRI RETE

neuroni = str2num(get(handles.edit12,'String'))
lr= str2num(get(handles.edit14,'String'))
epochs=str2num(get(handles.edit15,'String'))
show =str2num(get(handles.edit16,'String'))

%-------GENERAZIONE RETE E LEARNING
Rete = newff(input_train',(output_train(:,1))',neuroni); %creo nuova rete
Rete.trainParam.show= show; %imposto parametro show
Rete.trainParam.lr=lr; %imposto parametro lr
Rete.trainParam.epochs=epochs; %imposto parametro epochs
Rete = train(Rete,input_train',(output_train(:,1))'); %learning rete

output_rete= Rete(input_train') %genero output dalla rete post learning
assignin('base','output_rete',output_rete)
performancel =perform(Rete, output_rete, (output_train(:,1))') 
set(handles.edit17,'String',num2str(performancel))

output_rete_t= Rete(input_test') %genero output dalla rete post leatning passandogli gli input di test
assignin('base','output_rete_t',output_rete_t)
performancet =perform(Rete, output_rete_t, (output_test(:,1))') 
set(handles.edit18,'String',num2str(performancet))

set(handles.edit20,'String',avviso)

%-------- ERRORI

errore_l = output_rete - (output_train(:,1)')
assignin('base','errore_l',errore_l)

errore_t = output_rete_t - (output_test(:,1)')
assignin('base','errore_t',errore_t)



end
